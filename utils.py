def add(a: int, b: int):
    """2ta sonni yig'indisini topadi"""
    return a + b




def subtract(a: int, b: int):  
    """2ta sonni ayirmani topadi"""
    return a - b



def multiply(a: int, b: int):
    """2ta sonni ko'paytmasini topadi"""    
    return a * b






def divide(a: int, b: int):
    """2ta sonni bo'linmasini topadi"""
    if b == 0:
        print("Sonni 0ga bo'lib bo'lmaydi.")
    return a // b





def power(a: int, b: int):
   """2ta sonni darajasini topadi"""     
   return a ** b

